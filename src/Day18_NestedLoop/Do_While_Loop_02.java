package Day18_NestedLoop;

public class Do_While_Loop_02 {
    public static void main(String[] args) {

        int num = 103;

        while (num >= 100){
            System.out.print(num+" ");
            num --;

        }
        System.out.println("\n________________________");
        int num2 = 103;
        do {
            System.out.print(num2+" ");
            num2--;
        }while (num2 >=100);
    }
}
