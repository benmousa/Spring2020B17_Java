package Day05_Unary_ShorthandOperators;

public class UnaryOpereators {
    public static void main(String[] args) {

        int Z = 100;
        System.out.println(Z);
        System.out.println(++Z);
        System.out.println(Z);

        int p = 100;
        System.out.println(p++);
        System.out.println(p);


    }
}
